/* Создайте массив из 5 чисел. Используя цикл foreach и условные операторы ( if-else ), проверьте, является ли каждое число четным или нечетным, и выведите соответствующее сообщение. */
echo "\n\n";
// Создаем массив из 5 чисел
$numbers = [10, 15, 22, 33, 42];

// Проходим по каждому числу массива
foreach ($numbers as $number) {
    // Проверяем, является ли число четным
    if ($number % 2 == 0) {
        echo "$number - Четное число", "\n";
    } else {
        echo "$number - Нечетное число", "\n";
    }
}
echo "\n\n";

// Задание 2: Подсчет положительных и отрицательных чисел
/* Создайте массив из 7 чисел, содержащий как положительные, так и отрицательные значения. С помощью цикла foreach и ветвления ( if-else ) подсчитайте, сколько в массиве положительных и отрицательных чисел. */
// Создаем массив из 7 чисел
$numbers = [10, -5, 3, -8, 15, -2, 7];

// Инициализируем счетчики положительных и отрицательных чисел
$positiveCount = 0;
$negativeCount = 0;

// Проходим по каждому числу массива
foreach ($numbers as $number) {
    // Проверяем, является ли число положительным
    if ($number > 0) {
        $positiveCount++; // Увеличиваем счетчик положительных чисел
    } elseif ($number < 0) {
        $negativeCount++; // Увеличиваем счетчик отрицательных чисел
    }
}

// Выводим результат
echo "Положительных чисел: $positiveCount", "\n";
echo "Отрицательных чисел: $negativeCount", "\n";
echo "\n\n";

// Задание 3: Таблица умножения
/* Используя цикл for , выведите таблицу умножения для числа 5 от 1 до 10. Каждый результат должен выводиться на новой строке в формате: "5 * 1 = 5" . */
//
// Число для таблицы умножения
$number = 5;

// Цикл для умножения числа от 1 до 10
for ($i = 1; $i <= 10; $i++) {
    // Вывод результата умножения
    echo "$number * $i = " . ($number * $i) . "\n";
}
echo "\n\n";

// Задание 4: Условное отображение оценок студентов
/* Создайте массив с именами 3 студентов и их оценками за экзамен (например: ["Иван" => 85, "Мария" => 74, "Алексей" => 90] ). Используя цикл foreach и ветвления ( if-else ), напишите логику, которая определяет, сдал ли каждый студент экзамен:
Если оценка 50 и выше — вывести: "<Имя студента> сдал экзамен!" 
Если оценка ниже 50 — вывести: "<Имя студента> не сдал экзамен!" */
//
// Массив с именами студентов и их оценками
$students = [
    "Иван" => 85,
    "Мария" => 74,
    "Алексей" => 90
];

// Проходим по каждому студенту в массиве
foreach ($students as $name => $grade) {
    // Проверяем, сдал ли студент экзамен
    if ($grade >= 50) {
        echo "$name сдал экзамен!", "\n";
    } else {
        echo "$name не сдал экзамен!", "\n";
    }
}
echo "\n\n";

// Задание 5: Поиск минимального и максимального значений
/* Создайте массив из 8 случайных чисел. Используя цикл foreach , найдите и выведите минимальное и максимальное значения в массиве. */
//
// Создаем массив из 8 случайных чисел
$numbers = [];
for ($i = 0; $i < 8; $i++) {
    $numbers[] = rand(1, 100); // Случайные числа от 1 до 100
}

// Инициализируем минимальное и максимальное значения первыми элементами массива
$min = $numbers[0];
$max = $numbers[0];

// Проходим по массиву и находим минимальное и максимальное значения
foreach ($numbers as $number) {
    if ($number < $min) {
        $min = $number;
    }
    if ($number > $max) {
        $max = $number;
    }
}

// Выводим результат
echo "Массив чисел: " . implode(", ", $numbers) . "\n";
echo "Минимальное значение: $min", "\n";
echo "Максимальное значение: $max", "\n";
echo "\n\n";
?>